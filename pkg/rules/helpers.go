package rules

import "strconv"

// ordinalString returns the ordinal representation of an integer.
// Stolen from: https://github.com/dustin/go-humanize.
func ordinalString(x int) string {
	suffix := "th"
	switch x % 10 {
	case 1:
		if x%100 != 11 {
			suffix = "st"
		}
	case 2:
		if x%100 != 12 {
			suffix = "nd"
		}
	case 3:
		if x%100 != 13 {
			suffix = "rd"
		}
	}
	return strconv.Itoa(x) + suffix
}
